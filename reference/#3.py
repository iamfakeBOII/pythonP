#NUMERIC LITERAL

a = 0b10001111 #use '0b' before typing the binary number - BINARY LITERAL
b = 100.4  # DECIMAL LITERAL
c = 0o310  #use '0o' before typing the octal number - OCTAL LITERAL
d = 0x12c  #use '0x' before typing the hexadecimal number - HEXA DECIMAL LITERAL
e = 1233   # POSITIVE INTEGER LITERAL
f = -123   # NEGATIVE INTEGER LITERAL
print(a,b,c,d,e,f)


